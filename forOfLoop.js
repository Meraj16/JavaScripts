
// For in loop

let colors = ['red','blue','green','yellow']
for ( let i in colors){
  console.log(colors[i])
}

// For of loop

for(let color of colors){
  console.log(color)
}

//with the help of for of loop we can access direct value of any array

let letters = 'ABCDEFG'
for(let letter of letters){
  console.log(letter)
}